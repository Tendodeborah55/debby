public class Dreams {
    String Type;
    String Department;
    String WorkStation;

    public Dreams(String Type, String Department, String WorkStation) {
        this.Type = Type;
        this.Department = Department;
        this.WorkStation = WorkStation;

    }
    public String getType() {
        return Type;
    }
    public String getDepartment() {
        return Department;
    }

    public String getWorkStation() {
        return WorkStation;
    }
}
